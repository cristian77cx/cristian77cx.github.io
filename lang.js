// Función para cambiar idioma
function changeLang(lang) {
  const t = translations[lang];
  
  // Nav
  const navLinks = document.querySelectorAll('nav ul li a');
  navLinks[0].textContent = t.nav1;
  navLinks[1].textContent = t.nav2;
  navLinks[2].textContent = t.nav3;
  navLinks[3].textContent = t.nav4;
  navLinks[4].textContent = t.nav5;
  
  // Hero
  document.querySelector('.status').innerHTML = `<i class="fas fa-circle"></i> ${t.status}`;
  const subs = document.querySelectorAll('.hero-subtitle .gradient');
  subs[0].textContent = t.heroSub1;
  subs[1].textContent = t.heroSub2;
  subs[2].textContent = t.heroSub3;
  subs[3].textContent = t.heroSub4;
  document.querySelector('.hero-description').textContent = t.heroDesc;
  const stats = document.querySelectorAll('.stat-label');
  stats[0].textContent = t.stat1;
  stats[1].textContent = t.stat2;
  stats[2].textContent = t.stat3;
  document.querySelector('.hero-actions .primary').innerHTML = `<i class="fas fa-rocket"></i> ${t.btnProjects}`;
  document.querySelector('#request-full-cv').innerHTML = `<i class="fas fa-file-alt"></i> ${t.btnCV}`;
  document.querySelector('.hero-scroll span').textContent = t.scroll;
  
  // About
  document.querySelectorAll('.section-tag')[0].textContent = t.aboutTag;
  document.querySelectorAll('.section h2')[0].innerHTML = `<i class="fas fa-user"></i> ${t.aboutTitle}`;
  document.querySelectorAll('.section-desc')[0].textContent = t.aboutDesc;
  const cards = document.querySelectorAll('.info-card');
  cards[0].querySelector('h3').textContent = t.aboutCard1;
  cards[0].querySelector('p').textContent = t.aboutCard1Desc;
  cards[1].querySelector('h3').textContent = t.aboutCard2;
  cards[1].querySelector('p').innerHTML = `${t.aboutCard2Desc}<br><span class="muted-small">${t.aboutCard2Sub}</span>`;
  cards[2].querySelector('h3').textContent = t.aboutCard3;
  cards[2].querySelector('p').innerHTML = `${t.aboutCard3Desc}<br><span class="muted-small">${t.aboutCard3Sub}</span>`;
  cards[3].querySelector('h3').textContent = t.aboutCard4;
  cards[3].querySelector('p').innerHTML = `${t.aboutCard4Desc}<br><span class="muted-small">${t.aboutCard4Sub}</span>`;
  
  // Skills
  document.querySelectorAll('.section-tag')[1].textContent = t.skillsTag;
  document.querySelectorAll('.section h2')[1].innerHTML = `<i class="fas fa-code"></i> ${t.skillsTitle}`;
  document.querySelectorAll('.section-desc')[1].textContent = t.skillsDesc;
  const skillCats = document.querySelectorAll('.skill-category h3');
  skillCats[0].innerHTML = `<i class="fas fa-code"></i> ${t.skill1}`;
  skillCats[1].innerHTML = `<i class="fas fa-chart-line"></i> ${t.skill2}`;
  skillCats[2].innerHTML = `<i class="fas fa-brain"></i> ${t.skill3}`;
  
  // Stack
  document.querySelectorAll('.section-tag')[2].textContent = t.stackTag;
  document.querySelectorAll('.section h2')[2].innerHTML = `<i class="fas fa-layer-group"></i> ${t.stackTitle}`;
  
  // Projects
  document.querySelectorAll('.section-tag')[3].textContent = t.projectsTag;
  document.querySelectorAll('.section h2')[3].innerHTML = `<i class="fas fa-project-diagram"></i> ${t.projectsTitle}`;
  document.querySelectorAll('.section-desc')[2].textContent = t.projectsDesc;
  const projs = document.querySelectorAll('.project-card');
  projs[0].querySelector('h3').textContent = t.proj1Title;
  projs[0].querySelector('p').textContent = t.proj1Desc;
  projs[0].querySelectorAll('.detail-item span')[0].textContent = t.proj1Det1;
  projs[0].querySelectorAll('.detail-item span')[1].textContent = t.proj1Det2;
  if (projs[0].querySelector('.project-btn')) {
    projs[0].querySelector('.project-btn').innerHTML = `<i class="fab fa-github"></i> ${t.proj1Btn}`;
  }
  projs[1].querySelector('h3').textContent = t.proj2Title;
  projs[1].querySelector('p').textContent = t.proj2Desc;
  projs[1].querySelectorAll('.detail-item span')[0].textContent = t.proj2Det1;
  projs[1].querySelectorAll('.detail-item span')[1].textContent = t.proj2Det2;
  projs[2].querySelector('h3').textContent = t.proj3Title;
  projs[2].querySelector('p').textContent = t.proj3Desc;
  projs[2].querySelectorAll('.detail-item span')[0].textContent = t.proj3Det1;
  projs[2].querySelectorAll('.detail-item span')[1].textContent = t.proj3Det2;
  
  // Contact
  document.querySelectorAll('.section-tag')[4].textContent = t.contactTag;
  document.querySelector('.contact-info h2').innerHTML = `<i class="fas fa-paper-plane"></i> ${t.contactTitle}`;
  document.querySelector('.contact-info > p').textContent = t.contactDesc;
  const contactItems = document.querySelectorAll('.contact-item h4');
  contactItems[0].textContent = t.contactEmail;
  contactItems[1].textContent = t.contactLocation;
  contactItems[2].textContent = t.contactPhone;
  document.querySelector('.contact-form h3').textContent = t.contactFormTitle;
  document.getElementById('name').placeholder = t.contactName;
  document.getElementById('email').placeholder = t.contactEmailPlaceholder;
  document.getElementById('message').placeholder = t.contactMessage;
  document.querySelector('.contact-form button').innerHTML = `<i class="fas fa-paper-plane"></i> ${t.contactBtn}`;
  
  // Footer
  document.querySelector('.footer p').textContent = `© 2026 Cristian Pineda — ${t.footer}`;
  
  // Actualizar botones
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  
  // Guardar preferencia
  localStorage.setItem('lang', lang);
}

// Inicializar
document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('lang') || 'es';
  changeLang(savedLang);
  
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => changeLang(btn.dataset.lang));
  });
});
